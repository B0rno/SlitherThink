package com.lmu.SlitherThink.save.structure;

/**
 * Classe représentant une technique de jeu.
 * Contient le nom et la description de la technique.
 */
public class stockageTechnique {
    private String name; // Nom de la technique
    private String description; // Description de la technique

    /**
     * Retourne le nom de la technique.
     *
     * @return Le nom de la technique.
     */
    public String getName() {
        return name;
    }

    /**
     * Retourne la description de la technique.
     *
     * @return La description de la technique.
     */
    public String getDescription() {
        return description;
    }
}
